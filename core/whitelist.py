# Паттерны для проверки (regex), которые не заменяем
WHITELIST_PATTERNS: list[re.Pattern] = [
    # Госорганы — "Министерство ... Российской Федерации"
    re.compile(
        r'Министерство\s+\w+(?:\s+\w+){0,3}\s+'
        r'(?:Российской\s+Федерации|РФ)',
        re.IGNORECASE,
    ),
    # "Федеральная служба/агентство ..."
    re.compile(
        r'Федеральн(?:ая|ое)\s+(?:служба|агентство)\s+\w+(?:\s+\w+){0,5}',
        re.IGNORECASE,
    ),
    # Суды: "... суд ... области/края/республики"
    re.compile(
        r'(?:Арбитражный|Районный|Городской|Областной|Краевой|Верховный|'
        r'Конституционный|Мировой)\s+суд',
        re.IGNORECASE,
    ),
    # Прокуратура
    re.compile(
        r'(?:Генеральная\s+)?[Пп]рокуратура\s+\w+(?:\s+\w+){0,3}',
    ),
    # Управление/Отделение ... по ... области
    re.compile(
        r'(?:Управление|Отделение|Инспекция)\s+(?:ФНС|ПФР|СФР|Росреестра|'
        r'Роспотребнадзора|МВД|ФССП|ФАС)',
        re.IGNORECASE,
    ),
]

# Слова-маркеры госорганов в контексте
WHITELIST_KEYWORDS: frozenset[str] = frozenset({
    "РФ", "России", "Российской", "Федерации", "государственный",
    "государственная", "государственное", "федеральный", "федеральная",
    "федеральное", "муниципальный", "муниципальная", "муниципальное",
})


def is_whitelisted_org(text: str) -> bool:
    """Проверяет, является ли текст госорганом из белого списка."""
    stripped = text.strip()

    # Прямое совпадение
    if stripped in WHITELIST_ORGS:
        return True

    # Проверка без учёта регистра
    lower = stripped.lower()
    for org in WHITELIST_ORGS:
        if org.lower() == lower:
            return True

    # Проверка по паттернам
    for pattern in WHITELIST_PATTERNS:
        if pattern.search(stripped):
            return True

    return False


def is_whitelisted_in_context(text: str, context: str) -> bool:
    """
    Проверяет, не является ли найденная сущность частью названия госоргана.
    context — текст вокруг найденного фрагмента.
    """
    if is_whitelisted_org(text):
        return True

    # Проверяем контекст
    ctx_lower = context.lower()
    for org in WHITELIST_ORGS:
        if org.lower() in ctx_lower:
            # Найденный текст является частью названия госоргана
            if text.lower() in org.lower():
                return True

    return False
